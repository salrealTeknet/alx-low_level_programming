#include <stdio.h>
/**
*add -addition
*@a:number
*@b:number
*return : Restult
*/
int add(int a, int b){
        return (a + b);
}
/**
*sub -subtraction
*@a:number
*@b:number
*return : Restult
*/
int sub(int a, int b){
        return(a-b);

}
/**
*mul -multiplication
*@a:number
*@b:number
*return : Restult
*/
int mul(int a, int b){
        return(a*b);


}
/**
*div -division
*@a:number
*@b:number
*return : Restult
*/
int div(int a, int b){
        return(a/b);

}
/**
*mod -modulus
*@a:number
*@b:number
*return : Restult
*/
int mod(int a, int b){
        return(a%b);

}

